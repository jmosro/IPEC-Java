    private void btngenerarcompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngenerarcompraActionPerformed
        if (!escampovacio()) {
            try {
                calculardescuentos();
                if (rdhombre.isSelected())
                    modelo.addRow(new Object[]{
                        cmbnacionalidad.getSelectedItem(),
                        "Hombre",
                        cmbtallas.getSelectedItem(),
                        txtprenda.getText(),
                        txtcantidad.getText(),
                        txtprecio.getText(),
                        txttotalpagar.getText()
                    });
                else if (rdmujer.isSelected())
                    modelo.addRow(new Object[]{
                        cmbnacionalidad.getSelectedItem(),
                        "Mujer",
                        cmbtallas.getSelectedItem(),
                        txtprenda.getText(),
                        txtcantidad.getText(),
                        txtprecio.getText(),
                        txttotalpagar.getText()
                    });
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error");
            }            
        } else {
            JOptionPane.showMessageDialog(null, "No debe existir un campo vacío");
        }
    }//GEN-LAST:event_btngenerarcompraActionPerformed

