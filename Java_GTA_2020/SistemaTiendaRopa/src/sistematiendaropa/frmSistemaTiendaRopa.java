    private void btnnetopagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnetopagarActionPerformed
        double sumatoria = 0;
        int totalrow = tabledetalles.getRowCount() - 1;
        for (int i = 0; i <= totalrow; i++) {
            String temp = String.valueOf(tabledetalles.getValueAt(i, 6));
            if(temp.equals("")) sumatoria += 0;
            else sumatoria += Double.parseDouble(temp);
        }
        txtnetopagar.setText("S. " + String.valueOf(sumatoria));
    }//GEN-LAST:event_btnnetopagarActionPerformed

