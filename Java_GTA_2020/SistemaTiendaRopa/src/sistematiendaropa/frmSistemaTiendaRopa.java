    private boolean escampovacio() {
        if (cmbnacionalidad.getSelectedIndex()==0 ||
            !(rdhombre.isSelected() || rdmujer.isSelected()) ||
            cmbtallas.getSelectedIndex()==0 ||
            txtprenda.getText().equals("") ||
            txtcantidad.getText().equals("") ||
            txtprecio.getText().equals("") )
            return true;
        return false;
    }
    
    private void calculartotal(double descuento) {
        double cantidad, precio, total;
        cantidad = Double.parseDouble(txtcantidad.getText());
        precio = Double.parseDouble(txtprecio.getText());
        total = (precio * cantidad) - ((precio * cantidad) * descuento);
        txttotalpagar.setText(String.valueOf(total));
    }

    private void calculardescuentos() {
        if (cmbnacionalidad.getSelectedIndex() == 2) { // 2 -> EXTRANJERO
            if (rdhombre.isSelected()) { // HOMBRE
                switch (cmbtallas.getSelectedIndex()) {
                    case 1: calculartotal(0.04); break; // 1 -> TALLA S
                    case 2: calculartotal(0.09); break; // 2 -> TABLA M
                    case 3: calculartotal(0.12); break; // 3 -> TABLA L
                    default: break;
                }
            } else if (rdmujer.isSelected()) { // MUJER
                switch (cmbtallas.getSelectedIndex()) {
                    case 1: calculartotal(0.05); break; // 1 -> TALLA S
                    case 2: calculartotal(0.07); break; // 2 -> TALLA M
                    case 3: calculartotal(0.10); break; // 3 -> TALLA L
                    default: break;
                }
            }
        } else if (cmbnacionalidad.getSelectedIndex() == 1) { // 1 -> COSTARRICENSE
            if (rdhombre.isSelected()) { // HOMBRE
                switch (cmbtallas.getSelectedIndex()) {
                    case 1: calculartotal(0.05); break; // 1 -> TALLA S
                    case 2: calculartotal(0.07); break; // 2 -> TABLA M
                    case 3: calculartotal(0.10); break; // 3 -> TABLA L
                    default: break;
                }
            } else if (rdmujer.isSelected()) { // MUJER
                switch (cmbtallas.getSelectedIndex()) {
                    case 1: calculartotal(0.04); break; // 1 -> TALLA S
                    case 2: calculartotal(0.09); break; // 2 -> TALLA M
                    case 3: calculartotal(0.12); break; // 3 -> TALLA L
                    default: break;
                }
            }
        }
    }

